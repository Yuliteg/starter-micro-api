const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const UserContactDetailsSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = Item = mongoose.model(
  'UserContactDetails',
  UserContactDetailsSchema
);
